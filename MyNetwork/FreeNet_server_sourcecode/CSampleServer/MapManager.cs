            // 이동한 타일의 범위내에 다른 유닛 가져오기.
            var rangeUnits = GetAllOtherUnit(unit);
            
            var removedUnits = unit.prevNearUnits.Where(i => !rangeUnits.Contains(i)).ToList(); // 삭제된 플레이어 리스트
            var AddedUnits = rangeUnits.Where(i => !unit.prevNearUnits.Contains(i)).ToList(); // 새로 추가된 플레이어 리스트
            
            // 추가 된 유저를 내 유닛에 알리기
            unit.ResponseAddNearUnit(AddedUnits);
            // 다른 유닛들에게 나를 알리기
            foreach (var otherUnit in AddedUnits)
            {
                otherUnit.ResponseAddNearUnit(new List<CUnit>() {unit});
            }
            
            // 삭제 된 유저를 내 유닛에 알리기.
            unit.ResponseRemoveNearUnit(removedUnits);
            // 다른 유닛에게 내가 삭제됬다고 알리기.
            foreach (var otherUnit in removedUnits)
            {
                otherUnit.ResponseRemoveNearUnit(new List<CUnit>() {unit});
            }
        }

        // 유닛이 셋팅 된 타일에서 삭제.
        public void RemoveUnitTile(CUnit unit)
        {
            for (int x = 0; x < mapInfos.GetLength(0); ++x)
            {
                for (int y = 0; y < mapInfos.GetLength(1); ++y)
                {
                    if (mapInfos[x, y].HasTargetObject(unit))
                    {
                        mapInfos[x, y].RemoveObjectBase(unit);
                    }
                }
            }
        }

        // 지정한 위치, 범위의 타일을 가져온다.
        public List<MapTile> GetOtherUnitFromRange(int currentX, int currentY, int range)
        {
            var maps = new List<MapTile>(); 
            
            for (int x = currentX - range; x <= currentX + range; ++x)
            {
                for (int y = currentY - range; y <= currentY + range; ++y)
                {
                    if (ExistsMapInfo(x, y) == false)
                        continue;
                    
                    maps.Add(mapInfos[x, y]);
                }
            }
            
            return maps;
        }

        // 지정된 유닛의 범위내의 유닛을 모두 취득.
        public List<CUnit> GetAllOtherUnit(CUnit unit)
        {
            var units = new List<CUnit>();
            var currentX = unit.stateData.posX;
            var currentY = unit.stateData.posY;
            var nearRange = unit.NearRange;
            
            for (int x = currentX - nearRange; x <= currentX + nearRange; ++x)
            {
                for (int y = currentY - nearRange; y <= currentY + nearRange; ++y)
                {
                    if (ExistsMapInfo(x, y) == false)
                        continue;

                    if (mapInfos[x, y].stayUnit.Contains(unit))
                    {
                        var currentPosUnits = mapInfos[x, y].stayUnit
                            .FindAll(p => p.playerData.playerId != unit.playerData.playerId);
                        units.AddRange(currentPosUnits);
                        continue;
                    }

                    if (mapInfos[x, y].stayUnit.Count <= 0)
                        continue;

                    units.AddRange(mapInfos[x, y].stayUnit);
                }
            }

            return units;
        }
        
        // 지정된 좌표, 범위의 유닛을 전부 취득.
        public List<CUnit> GetAllUnitByNearRange(int currentX, int currentY, int range)
        {
            var units = new List<CUnit>(); 
            
            for (int x = currentX - range; x <= currentX + range; ++x)
            {
                for (int y = currentY - range; y <= currentY + range; ++y)
                {
                    if (ExistsMapInfo(x, y) == false)
                        continue;
                    
                    units.AddRange(mapInfos[x, y].stayUnit);
                }
            }
            
            return units;
        }

        // center유저의 범위에 target이 있는지 체크.
        public bool HasRangeInUnit(CUnit center, CUnit target)
        {
            var targets = GetAllOtherUnit(center);
            
            return targets.Contains(target);
        }
        
        public bool HasRangeInUnit(CUnit center, CUnit target, int centerRange)
        {
            var targets = GetAllUnitByNearRange(center.stateData.posX, center.stateData.posY, centerRange);

            return targets.Contains(target);
        }

        public bool HasUnit(int x, int y)
        {
            return mapInfos[x, y].stayUnit.Count != 0;
        }

        public bool HasUnit(int x, int y, UnitType type)
        {
            return mapInfos[x, y].stayUnit.Exists(p => p.playerData.unitType == (byte) type);
        }

        public List<CUnit> GetUnits(int x, int y)
        {
            return mapInfos[x, y].stayUnit;
        }

        // A유닛과 B유닛의 범위안에 유닛을 리턴.
        public List<CUnit> GetUnitFromUnits(CUnit unitA, CUnit unitB)
        {
            var resultList = new List<CUnit>();
            var nearA = unitA.GetNearRangeUnit();
            var nearB = unitB.GetNearRangeUnit();
            var AddedUnits = nearB.Where(i => !nearA.Contains(i)).ToList(); // 새로 추가된 플레이어 리스트

            resultList.AddRange(nearA);
            resultList.AddRange(AddedUnits);
            
            return resultList;
        }

        // 맵 배열 널 체크
        public bool ExistsMapInfo(int x, int y)
        {
            if (x < 0 || x >= mapInfos.GetLength(0))
                return false;

            if (y < 0 || y >= mapInfos.GetLength(1))
                return false;
            
            return true;
        }
        
        public void GetRandomPosition(int centerX, int centerY, int range, out int x, out int y)
        {
            int checkMaxCount = 100;

            while (checkMaxCount <= 100)
            {
                checkMaxCount++;
                var spawnPosX = random.Next(centerX - range, centerX + range);
                var spawnPosY = random.Next(centerY - range, centerY + range);

                if (ExistsMapInfo(spawnPosX, spawnPosY) == false)
                    continue;

                if (HasUnit(spawnPosX, spawnPosY))
                    continue;

                x = spawnPosX;
                y = spawnPosY;
                return;
            }
            
            x = random.Next(centerX - range, centerX + range);
            y = random.Next(centerY - range, centerY + range);
        }

        public List<GridPoint> FindPath(int startX, int startY, int destX, int destY)
        {
            var start = new GridPoint(startX, startY);
            var goal = new GridPoint(destX, destY);
            var path = new PathFinder();
            return path.FindPath(MapTileInfo, start, goal);
        }

        public UnitDirection SetDirectionByPosition(int currentX, int currentY, int destX, int destY)
        {
            if (currentX > destX && currentY > destY)
            {
                return UnitDirection.DOWN_LEFT; //
            }
            if (currentX > destX && currentY == destY)
            {
                return UnitDirection.LEFT; //
            }
            if (currentX > destX && currentY < destY)
            {
                return UnitDirection.UP_LEFT; //
            }
            if (currentX == destX && currentY < destY)
            {
                return UnitDirection.UP; //
            }
            if (currentX < destX && currentY < destY)
            {
                return UnitDirection.UP_RIGHT; //
            }
            if (currentX < destX && currentY == destY)
            {
                return UnitDirection.RIGHT; //
            }
            if (currentX < destX && currentY > destY)
            {
                return UnitDirection.DOWN_RIGHT;
            }
            if (currentX == destX && currentY > destY)
            {
                return UnitDirection.DOWN; //
            }

            return UnitDirection.UP;
        }
